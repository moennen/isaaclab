def main():
    parser = argparse.ArgumentParser(
        description="Replay Franka cube pick sequences with batched Newton FK + full physics."
    )
    parser.add_argument("--input",       type=str, default=str(_OUTPUTS_DIR / "reference_ik_sequences.json"),
                        help="Path to reference_ik_sequences.json (Tool 1 output).")
    parser.add_argument("--output",      type=str, default=str(_OUTPUTS_DIR / "reference_ik_replay.json"),
                        help="Path for replay output JSON.")
    parser.add_argument("--num-worlds",  type=int, default=16,
                        help="Batch size — number of sequences replayed simultaneously.")
    parser.add_argument("--sequence_id", type=str, default=None,
                        help="If set, replay only this sequence ID (num-worlds ignored).")
    parser.add_argument("--device",      type=str, default="cpu",
                        help="Torch device for reward tensors (cpu/cuda).")
    args = parser.parse_args()

    device = torch.device(args.device)

    urdf_path = _find_panda_urdf()
    print(f"[replay] Panda URDF: {urdf_path}")

    print(f"[replay] Building batched model (num_worlds={args.num_worlds}) ...")
    robot_mb, hand_local_idx, robot_body_count = build_robot_builder(urdf_path)
    batched_model, solver = build_batched_model(robot_mb, args.num_worlds)

    num_bodies_per_world = batched_model.body_count // args.num_worlds
    cube_local_idx       = robot_body_count   # cube added right after robot bodies

    n_coord_per_world = batched_model.joint_coord_count // args.num_worlds
    n_ctrl_per_world  = len(batched_model.control().joint_target_pos) // args.num_worlds
    print(f"[replay] Batched model: {batched_model.body_count} bodies  "
          f"({num_bodies_per_world} per world)  "
          f"{n_coord_per_world} coords/world  {n_ctrl_per_world} ctrl DOFs/world")
    print(f"[replay] hand_local_idx={hand_local_idx}  cube_local_idx={cube_local_idx}")
    print(f"[replay] Solver: {type(solver).__name__}")
    print(f"[replay] Substeps: {_N_SUBSTEPS_PER_FRAME} × {_SUB_DT*1000:.1f}ms = "
          f"{_N_SUBSTEPS_PER_FRAME*_SUB_DT*1000:.0f}ms per frame")

    data      = load_sequences(args.input)
    cfg       = data["config"]
    sequences = data["sequences"]

    if args.sequence_id is not None:
        sequences = [s for s in sequences if s["id"] == args.sequence_id]
        if not sequences:
            raise ValueError(f"Sequence '{args.sequence_id}' not found in {args.input}")
        # Single-sequence path: use num_worlds=1
        args.num_worlds = 1
        batched_model, solver = build_batched_model(robot_mb, 1)
        num_bodies_per_world  = batched_model.body_count
        cube_local_idx        = robot_body_count
        print(f"[replay] Single-sequence mode (num_worlds=1)")

    num_worlds = args.num_worlds
    state_0    = batched_model.state()
    state_1    = batched_model.state()
    control    = batched_model.control()

    default_robot_q = np.array(_HOME_JOINT_Q, dtype=np.float32)

    n_seq    = len(sequences)
    n_frames = len(sequences[0]["frames"])
    n_batch  = (n_seq + num_worlds - 1) // num_worlds

    print(f"[replay] Replaying {n_seq} sequences in batches of {num_worlds} "
          f"| {n_frames} frames × {_N_SUBSTEPS_PER_FRAME*_SUB_DT*1000:.0f}ms = "
          f"{n_frames * _N_SUBSTEPS_PER_FRAME * _SUB_DT:.1f}s per sequence")

    all_results = []
    seq_idx     = 0

    for batch_num in range(n_batch):
        active_count = min(num_worlds, n_seq - seq_idx)
        pad_count    = num_worlds - active_count

        batch_seqs = sequences[seq_idx : seq_idx + active_count]
        if pad_count > 0:
            batch_seqs = batch_seqs + [batch_seqs[0]] * pad_count   # pad with first real seq

        # Header
        desc = " | ".join(
            f"{s['id']}({label_description(s['label'])})"
            for s in batch_seqs[:active_count]
        )
        print(f"[replay] batch {batch_num + 1}/{n_batch} "
              f"({active_count} active, {pad_count} pad): {desc}", flush=True)

        # Reset
        cube_positions = np.array([s["cube_init_pos_w"] for s in batch_seqs], dtype=np.float32)
        reset_batch_state(batched_model, state_0, state_1, cube_positions, default_robot_q, num_worlds)
        newton.eval_fk(batched_model, state_0.joint_q, state_0.joint_qd, state_0)

        # Run batch
        batch_results = run_batch_replay(
            batched_model=batched_model,
            solver=solver,
            hand_local_idx=hand_local_idx,
            cube_local_idx=cube_local_idx,
            num_bodies_per_world=num_bodies_per_world,
            num_worlds=num_worlds,
            state_0=state_0,
            state_1=state_1,
            control=control,
            batch_seqs=batch_seqs,
            active_count=active_count,
            cfg=cfg,
            device=device,
        )

        # Print per-sequence summary
        for w, result in enumerate(batch_results):
            ep_total = result["episode_rewards"]["total"]
            expected = "SUCCESS" if result["expected_high_reward"] else "FAILURE"
            actual   = "SUCCESS" if result["success_frame"] >= 0 else "FAILURE"
            status   = "OK" if actual == expected else "MISMATCH"
            sf       = result["success_frame"]
            variance = result["joint_pos_variance_mean"]
            print(f"  [{result['id']}] frames={n_frames}  "
                  f"episode_reward={ep_total:+.1f}  expected={expected}  [{status}]  "
                  f"success_frame={sf}  variance={variance:.4f}")

        all_results.extend(batch_results)
        seq_idx += active_count

    output = {
        "version":     "1.0",
        "replayed_at": datetime.datetime.now().isoformat(),
        "source":      args.input,
        "config":      cfg,
        "sequences":   all_results,
    }
    save_replay(output, args.output)


if __name__ == "__main__":
    main()
