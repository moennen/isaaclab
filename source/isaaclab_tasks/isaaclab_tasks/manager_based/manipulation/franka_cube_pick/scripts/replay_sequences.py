"""Tool 2 — Sequence Replayer (batched multi-world Newton FK + full physics).

Replays sequences generated by generate_sequences.py, driving a fresh Newton
physics model with the recorded joint_pos_cmd targets, then computing per-frame
rewards and writing them to a JSON output file.

Architecture
------------
Runs N worlds simultaneously via begin_world/add_builder/end_world, mirroring
generate_sequences.py. All N sequences are stepped in a single solver.step() call
per substep — the same GPU parallelism as the generator.

Robot motion: PD controller tracks the recorded joint_pos_cmd targets.
  FK-equivalent: same commands → same physics → same cube trajectory.

Cube physics: full Newton physics simulation — gravity, contact forces, friction.
  No analytic cube teleportation or kinematic overrides.

Physics model mirrors the generator's phys_model exactly:
  - Same PD gains, armature, effort limits
  - Same gravity compensation (mujoco:jnt_actgravcomp + mujoco:gravcomp)
  - Same finger BOX shapes + collision groups
  - Same contact parameters (ke, kd, kf, mu)
  - Same solver (SolverMuJoCo, impratio=1000, cone=elliptic)
  - Same substep count: 10 × 2ms per recorded frame
    (generator: 10 substeps per outer step × record_every=1 = 10 substeps/frame)

Batch processing
----------------
The outer loop processes sequences in batches of --num-worlds (default 16).
Each batch:
  1. Reset all N worlds: robot → _HOME_JOINT_Q, cubes → their init positions.
  2. For each of the 500 recorded frames:
       a. Set control commands from each world's recorded joint_pos_cmd.
       b. Run 10 physics substeps (one solver.step per substep, all N worlds).
       c. Read body_q / joint_q for all N worlds in one GPU→CPU sync.
       d. Compute rewards for all N worlds in one vectorized torch call.
  3. Collect per-world results.
Last batch is zero-padded if num_sequences % num_worlds != 0.

Usage
-----
python replay_sequences.py \\
    --input  data/validation/sequences.json \\
    --output data/validation/replay.json \\
    [--num-worlds 16]          # batch size (default: 16)
    [--sequence_id seq_0003]   # replay a single sequence (default: all)
"""

