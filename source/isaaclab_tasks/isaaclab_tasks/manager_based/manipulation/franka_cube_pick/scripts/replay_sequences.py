"""Tool 2 — Sequence Replayer (Newton FK + full physics).

Replays sequences generated by generate_sequences.py, driving a fresh Newton
physics model with the recorded joint_pos_cmd targets, then computing per-frame
rewards and writing them to a JSON output file.

Architecture
------------
Runs fully standalone using Newton physics — no Isaac Sim / AppLauncher required.

Robot motion: PD controller tracks the recorded joint_pos_cmd targets exactly
  as in the generator.  This is equivalent to FK: the robot follows the same
  joint-angle trajectory that was commanded during generation.

Cube physics: full Newton physics simulation — gravity, contact forces, friction.
  No analytic cube teleportation or kinematic overrides.

Physics model mirrors the generator's phys_model exactly:
  - Same PD gains, armature, effort limits
  - Same gravity compensation (mujoco:jnt_actgravcomp + mujoco:gravcomp)
  - Same finger BOX shapes + collision groups
  - Same contact parameters (ke, kd, kf, mu)
  - Same solver (SolverMuJoCo, impratio=1000, cone=elliptic)
  - Same substep count: 20 × 2ms per recorded frame
    (generator: 10 substeps per outer step × record_every=2 outer steps = 20 substeps)

Usage
-----
python replay_sequences.py \\
    --input  data/validation/sequences.json \\
    --output data/validation/replay.json \\
    [--sequence_id seq_0003]   # replay a single sequence (default: all)
"""

