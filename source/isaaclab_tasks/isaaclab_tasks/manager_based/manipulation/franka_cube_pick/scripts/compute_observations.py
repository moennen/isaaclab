def main():
    parser = argparse.ArgumentParser(
        description="Compute full 47-dim RL obs from scripted sequences (batched Newton)."
    )
    parser.add_argument("--input",      type=str,
                        default=str(_OUTPUTS_DIR / "reference_ik_sequences.json"),
                        help="Path to reference_ik_sequences.json (Tool 1 output).")
    parser.add_argument("--output",     type=str,
                        default=str(_OUTPUTS_DIR / "reference_ik_observations.json"),
                        help="Output path for observations JSON.")
    parser.add_argument("--num-worlds", type=int, default=16,
                        help="Batch size (default: 16).")
    parser.add_argument("--sequence_id", type=str, default=None,
                        help="If set, compute observations for this sequence only.")
    args = parser.parse_args()

    urdf_path = _find_panda_urdf()
    print(f"[obs-compute] Panda URDF: {urdf_path}")

    print(f"[obs-compute] Building batched model (num_worlds={args.num_worlds}) ...")
    robot_mb, hand_local_idx, robot_body_count = build_robot_builder(urdf_path)
    batched_model, solver = build_batched_model(robot_mb, args.num_worlds)

    num_bodies_per_world = batched_model.body_count // args.num_worlds
    cube_local_idx       = robot_body_count
    n_coord_per_world    = batched_model.joint_coord_count // args.num_worlds
    n_dof_per_world      = batched_model.joint_dof_count   // args.num_worlds

    print(f"[obs-compute] {batched_model.body_count} bodies total  "
          f"({num_bodies_per_world}/world)  "
          f"{n_coord_per_world} coords/world  {n_dof_per_world} DOFs/world")
    print(f"[obs-compute] hand_local_idx={hand_local_idx}  cube_local_idx={cube_local_idx}")
    print(f"[obs-compute] obs_dims={len(OBS_NAMES)}: {OBS_NAMES}")

    data      = load_sequences(args.input)
    cfg       = data["config"]
    sequences = data["sequences"]

    if args.sequence_id is not None:
        sequences = [s for s in sequences if s["id"] == args.sequence_id]
        if not sequences:
            raise ValueError(f"Sequence '{args.sequence_id}' not found.")
        args.num_worlds = 1
        batched_model, solver = build_batched_model(robot_mb, 1)
        num_bodies_per_world  = batched_model.body_count
        cube_local_idx        = robot_body_count
        print(f"[obs-compute] Single-sequence mode (num_worlds=1)")

    num_worlds = args.num_worlds
    state_0    = batched_model.state()
    state_1    = batched_model.state()
    control    = batched_model.control()

    default_robot_q = np.array(_HOME_JOINT_Q, dtype=np.float32)

    n_seq    = len(sequences)
    n_frames = len(sequences[0]["frames"])
    n_batch  = (n_seq + num_worlds - 1) // num_worlds

    print(f"[obs-compute] {n_seq} sequences × {n_frames} frames | "
          f"batches={n_batch}  substeps={_N_SUBSTEPS_PER_FRAME}×{_SUB_DT*1000:.1f}ms/frame")

    all_results = []
    seq_idx = 0

    for batch_num in range(n_batch):
        active_count = min(num_worlds, n_seq - seq_idx)
        pad_count    = num_worlds - active_count
        batch_seqs   = sequences[seq_idx : seq_idx + active_count]
        if pad_count > 0:
            batch_seqs = batch_seqs + [batch_seqs[0]] * pad_count

        desc = " | ".join(
            f"{s['id']}({label_description(s['label'])})"
            for s in batch_seqs[:active_count]
        )
        print(f"[obs-compute] batch {batch_num + 1}/{n_batch} "
              f"({active_count} active, {pad_count} pad): {desc}", flush=True)

        cube_positions = np.array([s["cube_init_pos_w"] for s in batch_seqs], dtype=np.float32)
        reset_batch_state(batched_model, state_0, state_1, cube_positions, default_robot_q, num_worlds)
        newton.eval_fk(batched_model, state_0.joint_q, state_0.joint_qd, state_0)

        batch_results = run_batch_obs(
            batched_model=batched_model,
            solver=solver,
            hand_local_idx=hand_local_idx,
            cube_local_idx=cube_local_idx,
            num_bodies_per_world=num_bodies_per_world,
            num_worlds=num_worlds,
            state_0=state_0,
            state_1=state_1,
            control=control,
            batch_seqs=batch_seqs,
            active_count=active_count,
            cfg=cfg,
        )

        all_results.extend(batch_results)
        seq_idx += active_count

    output = {
        "version":       "1.0",
        "computed_at":   datetime.datetime.now().isoformat(),
        "source":        args.input,
        "config":        cfg,
        "obs_names":     OBS_NAMES,
        "obs_dims":      len(OBS_NAMES),
        "quat_convention":      "xyzw (Newton/MuJoCo convention)",
        "body_qd_convention":   "[omega_xyz(3), linvel_xyz(3)] world frame at CoM",
        "sequences":     all_results,
    }

    out_path = Path(args.output)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    import json
    with open(out_path, "w") as f:
        json.dump(output, f, indent=2)
    print(f"[obs-compute] Saved {n_seq} sequences → {out_path}")


if __name__ == "__main__":
    main()
