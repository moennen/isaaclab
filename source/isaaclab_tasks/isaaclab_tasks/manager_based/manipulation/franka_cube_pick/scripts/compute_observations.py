"""Tool 6 — Observation Computer (batched multi-world Newton FK + full physics).

Replays sequences generated by generate_sequences.py and extracts the full
47-dim RL observation vector per frame — identical to what FrankaCubePickEnvCfg
would present to the policy during training.

Architecture mirrors replay_sequences.py exactly (same Newton model, same
physics parameters, same batched multi-world setup). The only difference is
the output: instead of per-frame rewards, each frame stores the full obs vector.

Observation vector (47 dims, matching FrankaCubePickEnvCfg.ObservationsCfg)
---------------------------------------------------------------------------
  cube_pos  (3) : cube XYZ in robot root frame  [m]
  cube_quat (4) : cube orientation in robot root frame  [xyzw — Newton convention]
  ee_pos    (3) : EE position in robot root frame  [m]
  ee_quat   (4) : EE orientation in robot root frame  [xyzw]
  ee_linvel (3) : EE linear velocity in robot root frame  [m/s]
  ee_angvel (3) : EE angular velocity in robot root frame  [rad/s]
  joint_pos (9) : arm (7) + finger (2) joint positions  [rad / m]
  joint_vel (9) : arm (7) + finger (2) joint velocities  [rad/s / m/s]
  last_action(9): joint_pos_cmd at this step  (RL last_action is previous step;
                  scripted sequences use current step's IK command as proxy)

Robot root frame:
  Robot is fixed at world origin with identity rotation.
  → robot root frame == world frame for this task.
  → No rotation transformation needed: cube_pos_b = cube_pos_w − robot_pos_w.

Newton conventions:
  Quaternion format: [qx, qy, qz, qw]  (xyzw)
  body_qd layout: [omega_x, omega_y, omega_z, vx, vy, vz]  (angular then linear, world frame)

Usage
-----
python compute_observations.py \\
    --input  data/validation/reference_ik_sequences.json \\
    --output data/validation/reference_ik_observations.json \\
    [--num-worlds 16]
"""

