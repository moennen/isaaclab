def _find_panda_urdf() -> Path:
    for p in _FRANKA_URDF_CANDIDATES:
        if p.exists():
            return p
    raise FileNotFoundError("Panda URDF not found. Install Isaac Sim pip package.")


def build_robot_builder(urdf_path: Path) -> tuple:
    mb = newton.ModelBuilder()
    newton.solvers.SolverMuJoCo.register_custom_attributes(mb)
    mb.add_urdf(str(urdf_path), floating=False, enable_self_collisions=False,
                parse_visuals_as_colliders=False)

    hand_local_idx    = next(i for i, k in enumerate(mb.body_label) if _PANDA_HAND_BODY_LABEL in k)
    left_finger_body  = next(i for i, k in enumerate(mb.body_label) if "leftfinger"  in k)
    right_finger_body = next(i for i, k in enumerate(mb.body_label) if "rightfinger" in k)

    gravcomp_dof = mb.custom_attributes["mujoco:jnt_actgravcomp"]
    if gravcomp_dof.values is None:
        gravcomp_dof.values = {}
    for dof_idx in range(7):
        gravcomp_dof.values[dof_idx] = True

    gravcomp_body = mb.custom_attributes["mujoco:gravcomp"]
    if gravcomp_body.values is None:
        gravcomp_body.values = {}
    for body_idx in range(2, mb.body_count):
        gravcomp_body.values[body_idx] = 1.0

    for i in range(len(mb.shape_body)):
        if mb.shape_body[i] >= 0:
            mb.shape_collision_group[i] = 2

    _cfg_fbox = newton.ModelBuilder.ShapeConfig()
    _cfg_fbox.ke = _CONTACT_KE
    _cfg_fbox.kd = _CONTACT_KD
    _cfg_fbox.kf = _CONTACT_KF
    _cfg_fbox.mu = _CONTACT_MU

    mb.add_shape_box(
        body=left_finger_body,
        xform=wp.transform(wp.vec3(0.0, 0.013, 0.027), wp.quat_identity()),
        hx=0.025, hy=0.013, hz=0.018,
        cfg=_cfg_fbox, label="leftfinger_box",
    )
    mb.shape_collision_group[len(mb.shape_body) - 1] = 1

    mb.add_shape_box(
        body=right_finger_body,
        xform=wp.transform(wp.vec3(0.0, -0.013, 0.027), wp.quat_identity()),
        hx=0.025, hy=0.013, hz=0.018,
        cfg=_cfg_fbox, label="rightfinger_box",
    )
    mb.shape_collision_group[len(mb.shape_body) - 1] = 1

    all_ke     = _ARM_KE      + _FINGER_KE
    all_kd     = _ARM_KD      + _FINGER_KD
    all_arm    = _ARM_ARMATURE + _FINGER_ARMATURE
    all_effort = _ARM_EFFORT  + _FINGER_EFFORT
    for i in range(_N_ROBOT_JOINTS):
        mb.joint_target_ke[i]    = float(all_ke[i])
        mb.joint_target_kd[i]    = float(all_kd[i])
        mb.joint_armature[i]     = float(all_arm[i])
        mb.joint_effort_limit[i] = float(all_effort[i])

    for i in range(_N_ROBOT_JOINTS):
        mb.joint_q[i]          = float(_HOME_JOINT_Q[i])
        mb.joint_target_pos[i] = float(_HOME_JOINT_Q[i])

    return mb, hand_local_idx, mb.body_count


def build_batched_model(robot_builder: newton.ModelBuilder, num_worlds: int) -> tuple:
    cfg_cube = newton.ModelBuilder.ShapeConfig()
    cfg_cube.density = _CUBE_DENSITY
    cfg_cube.ke      = _CONTACT_KE
    cfg_cube.kd      = _CONTACT_KD
    cfg_cube.kf      = _CONTACT_KF
    cfg_cube.mu      = _CONTACT_MU

    scene = newton.ModelBuilder()
    for _ in range(num_worlds):
        scene.begin_world()
        scene.add_builder(robot_builder)
        cube_body = scene.add_body(
            xform=wp.transform(wp.vec3(0.0, 0.0, _CUBE_HALF_SIZE), wp.quat_identity()),
            mass=0.0, label="cube",
        )
        scene.add_shape_box(
            body=cube_body,
            hx=_CUBE_HALF_SIZE, hy=_CUBE_HALF_SIZE, hz=_CUBE_HALF_SIZE,
            cfg=cfg_cube, label="cube_shape",
        )
        scene.shape_collision_group[len(scene.shape_body) - 1] = 1
        scene.end_world()

    scene.add_ground_plane()
    batched_model = scene.finalize()

    solver = newton.solvers.SolverMuJoCo(
        batched_model,
        solver="newton",
        integrator="implicitfast",
        iterations=20,
        ls_parallel=True,
        ls_iterations=100,
        nconmax=512 * num_worlds,
        njmax=1000 * num_worlds,
        cone="elliptic",
        impratio=1000.0,
    )
    return batched_model, solver


def reset_batch_state(batched_model, state_0, state_1, cube_positions_np, default_robot_q, num_worlds):
    n_coord = batched_model.joint_coord_count // num_worlds
    joint_q  = np.zeros(batched_model.joint_coord_count, dtype=np.float32)
    joint_qd = np.zeros(batched_model.joint_dof_count,   dtype=np.float32)
    for w in range(num_worlds):
        base = w * n_coord
        joint_q[base : base + _N_ROBOT_JOINTS] = default_robot_q
        cx, cy, cz = cube_positions_np[w]
        joint_q[base + _N_ROBOT_JOINTS     : base + _N_ROBOT_JOINTS + 3] = [cx, cy, cz]
        joint_q[base + _N_ROBOT_JOINTS + 3 : base + _N_ROBOT_JOINTS + 7] = [0., 0., 0., 1.]
    state_0.joint_q.assign(joint_q)
    state_0.joint_qd.assign(joint_qd)
    state_1.joint_q.assign(joint_q)
    state_1.joint_qd.assign(joint_qd)


def _simulate(solver, state_0, state_1, control, contacts, sub_dt, n_substeps):
    for _ in range(n_substeps):
        state_0.clear_forces()
        solver.step(state_0, state_1, control, contacts, sub_dt)
        state_0, state_1 = state_1, state_0


# ---------------------------------------------------------------------------
# Reachability helper (matches reward_utils.reachable_mask logic)
# ---------------------------------------------------------------------------

def _reachable_mask(cube_pos_np, robot_pos_np, r_min, r_max):
    """Per-world reachability mask — (N,) float32, 1.0=reachable."""
    diff = cube_pos_np[:, :2] - robot_pos_np[:, :2]
    dist = np.linalg.norm(diff, axis=1)
    return ((dist >= r_min) & (dist <= r_max)).astype(np.float32)


# ---------------------------------------------------------------------------
# Batch observation extraction
# ---------------------------------------------------------------------------

def run_batch_obs(
    batched_model,
    solver,
    hand_local_idx: int,
    cube_local_idx: int,
    num_bodies_per_world: int,
    num_worlds: int,
    state_0,
    state_1,
    control,
    batch_seqs: list,
    active_count: int,
    cfg: dict,
) -> list:
    """Run active_count sequences simultaneously; return per-sequence obs frame lists."""
    n_coord_per_world = batched_model.joint_coord_count // num_worlds
    n_dof_per_world   = batched_model.joint_dof_count   // num_worlds
    n_ctrl_per_world  = len(control.joint_target_pos)   // num_worlds
    n_frames = len(batch_seqs[0]["frames"])

    r_min = cfg["reachable_radius_min"]
    r_max = cfg["reachable_radius_max"]

    robot_pos_np = np.zeros((num_worlds, 3), dtype=np.float32)  # robot fixed at origin
    default_robot_q = np.array(_HOME_JOINT_Q, dtype=np.float32)

    # Per-world frame accumulator: list of lists (n_frames obs vectors per world)
    frame_obs_pw = [[] for _ in range(num_worlds)]

    contacts = None
    try:
        contacts = batched_model.collide(state_0)
    except AttributeError:
        if hasattr(batched_model, "collider"):
            contacts = batched_model.collider()

    for frame_idx in range(n_frames):
        # --- Apply recorded joint commands ---
        ctrl_np = np.zeros((num_worlds, n_ctrl_per_world), dtype=np.float32)
        for w in range(num_worlds):
            ctrl_np[w, :_N_ROBOT_JOINTS] = batch_seqs[w]["frames"][frame_idx]["joint_pos_cmd"]
        control.joint_target_pos.assign(ctrl_np.reshape(-1))

        # --- Step physics ---
        try:
            contacts = batched_model.collide(state_0)
        except AttributeError:
            pass
        _simulate(solver, state_0, state_1, control, contacts, _SUB_DT, _N_SUBSTEPS_PER_FRAME)

        # --- FK to populate body_q and body_qd ---
        newton.eval_fk(batched_model, state_0.joint_q, state_0.joint_qd, state_0)

        # --- One GPU→CPU sync for all state arrays ---
        body_q_np  = state_0.body_q.numpy()   # (n_bodies_total, 7): [px,py,pz, qx,qy,qz,qw]
        body_qd_np = state_0.body_qd.numpy()  # (n_bodies_total, 6): [omega_xyz, linvel_xyz]
        joint_q_np = state_0.joint_q.numpy()  # (n_coord_total,)
        joint_qd_np= state_0.joint_qd.numpy() # (n_dof_total,)

        # --- Extract per-world observations ---
        for w in range(num_worlds):
            gh   = w * num_bodies_per_world + hand_local_idx   # global body index: EE
            gc   = w * num_bodies_per_world + cube_local_idx   # global body index: cube

            # Cube state
            cube_pos_w  = body_q_np[gh if False else gc][:3]   # (3) position
            cube_quat_w = body_q_np[gc][3:7]                    # (4) [qx,qy,qz,qw]

            # EE state
            ee_pos_w  = body_q_np[gh][:3]   # (3)
            ee_quat_w = body_q_np[gh][3:7]  # (4) [qx,qy,qz,qw]
            # body_qd: [omega_x, omega_y, omega_z, vx, vy, vz] — world frame at CoM
            ee_angvel_w = body_qd_np[gh][0:3]  # (3) [rad/s]
            ee_linvel_w = body_qd_np[gh][3:6]  # (3) [m/s]

            # Joint state (robot DOF only — first _N_ROBOT_JOINTS of each world's slice)
            q_base  = w * n_coord_per_world
            qd_base = w * n_dof_per_world
            joint_pos = joint_q_np [q_base  : q_base  + _N_ROBOT_JOINTS]   # (9)
            joint_vel = joint_qd_np[qd_base : qd_base + _N_ROBOT_JOINTS]   # (9)

            # Last action = current step's joint_pos_cmd (proxy for RL last_action)
            last_action = ctrl_np[w, :_N_ROBOT_JOINTS]   # (9)

            # Robot root frame == world frame (robot fixed at origin)
            # cube_pos_b = cube_pos_w - robot_pos_w
            robot_pos_w = robot_pos_np[w]
            cube_pos_b  = cube_pos_w - robot_pos_w
            ee_pos_b    = ee_pos_w   - robot_pos_w

            # Reachability mask
            cube_dist_xy = float(np.linalg.norm(cube_pos_b[:2]))
            reach_mask   = 1.0 if (r_min <= cube_dist_xy <= r_max) else 0.0

            # Assemble 47-dim obs
            obs = np.concatenate([
                cube_pos_b,    # 3
                cube_quat_w,   # 4
                ee_pos_b,      # 3
                ee_quat_w,     # 4
                ee_linvel_w,   # 3
                ee_angvel_w,   # 3
                joint_pos,     # 9
                joint_vel,     # 9
                last_action,   # 9
            ]).astype(np.float32)   # (47,)

            assert len(obs) == 47

            frame_rec = batch_seqs[w]["frames"][frame_idx]
            frame_obs_pw[w].append({
                "step":          frame_rec["step"],
                "t":             frame_rec["t"],
                "obs":           obs.tolist(),
                "reachable_mask": reach_mask,
            })

    # Build result dicts for active worlds only
    results = []
    for w in range(active_count):
        results.append({
            "id":                    batch_seqs[w]["id"],
            "label":                 batch_seqs[w]["label"],
            "cube_init_pos_w":       batch_seqs[w]["cube_init_pos_w"],
            "cube_horizontal_dist":  batch_seqs[w]["cube_horizontal_dist"],
            "frames":                frame_obs_pw[w],
        })
    return results


